package com.ruoyi.system.domain;




public class Text {
    private String context;

    public String getContext() {
        return context;
    }

    public void setContext(String context) {
        this.context = context;
    }

    @Override
    public String toString() {
        return "Text{" +
                "context='" + context + '\'' +
                '}';
    }
}
